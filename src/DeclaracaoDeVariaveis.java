public class DeclaracaoDeVariaveis {
    public static void main(String[] args) {
        System.out.println("== VARIÁVEIS ==");
        int anoNascimento = 2008; //Crio as variáveis e dou um valorára elas
        double nota = 69.75;
        boolean ativo = true;
        char letra = 'D';

        System.out.println("Ano de nascimento: " + anoNascimento); //Utilizo o System.out.println para mostar os seus valores
        System.out.println("Nota:" + nota);
        System.out.println("Ativo: " + ativo);
        System.out.println("O Nome começa com: " + letra);
    }
}
